using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using System.Data;
using System.Data.OleDb;

namespace FAD3.Database.Classes.merge
{
    public class CatchLocalNameRepository
    {
        private FADEntities _fadEntities;
        public List<CatchLocalName> CatchLocalNames { get; set; }

        public CatchLocalNameRepository(FADEntities fadEntities)
        {
            _fadEntities = fadEntities;
            CatchLocalNames = getCatchLocalNames();
        }

        private List<CatchLocalName> getCatchLocalNames()
        {
            List<CatchLocalName> thisList = new List<CatchLocalName>();
            var dt = new DataTable();
            using (var conection = new OleDbConnection(_fadEntities.ConnectionString))
            {
                try
                {
                    conection.Open();
                    string query = $"Select * from tblBaseLocalNames";


                    var adapter = new OleDbDataAdapter(query, conection);
                    adapter.Fill(dt);
                    if (dt.Rows.Count > 0)
                    {
                        thisList.Clear();
                        foreach (DataRow dr in dt.Rows)
                        {
                            CatchLocalName cln = new CatchLocalName();
                            cln.Guid = dr["NameNo"].ToString();
                            cln.LocalName= dr["Name"].ToString();


                            thisList.Add(cln);
                        }
                    }
                }
                catch (Exception ex)
                {
                    Logger.Log(ex);

                }
                return thisList;
            }
        }

        public bool Add(CatchLocalName cln)
        {
            bool success = false;
            using (OleDbConnection conn = new OleDbConnection(_fadEntities.ConnectionString))
            {
                conn.Open();
                var sql = $@"Insert into tblBaseLocalNames (NameNo, Name)
                           Values 
                           ({{{cln.Guid}}},'{cln.LocalName}')";
                using (OleDbCommand update = new OleDbCommand(sql, conn))
                {
                    success = update.ExecuteNonQuery() > 0;
                }
            }
            return success;
        }

        public bool Update(CatchLocalName cln)
        {
            bool success = false;
            using (OleDbConnection conn = new OleDbConnection(_fadEntities.ConnectionString))
            {
                conn.Open();
                var sql = $@"Update tblBaseLocalNames set
                                Name = '{cln.LocalName}'
                            WHERE NameNo = {{{cln.Guid}}}";
                using (OleDbCommand update = new OleDbCommand(sql, conn))
                {
                    success = update.ExecuteNonQuery() > 0;
                }
            }
            return success;
        }

        public bool Delete(string id)
        {
            bool success = false;
            using (OleDbConnection conn = new OleDbConnection(_fadEntities.ConnectionString))
            {
                conn.Open();
                var sql = $"Delete * from tblBaseLocalNames where NameNo={{{id}}}";
                using (OleDbCommand update = new OleDbCommand(sql, conn))
                {
                    try
                    {
                        success = update.ExecuteNonQuery() > 0;
                    }
                    catch (OleDbException)
                    {
                        success = false;
                    }
                    catch (Exception ex)
                    {
                        Logger.Log(ex);
                        success = false;
                    }
                }
            }
            return success;
        }
    }
}
